// Helper function to get the current season's destinations
export const getCurrentSeasonDestinations = () => {
  const month = new Date().getMonth(); // 0-11

  // Return destinations based on current month
  if (month >= 11 || month <= 1) return seasonalDestinations.winter;      // Dec-Feb
  if (month >= 2 && month <= 4) return seasonalDestinations.spring;       // Mar-May
  if (month >= 5 && month <= 7) return seasonalDestinations.summer;       // Jun-Aug
  return seasonalDestinations.fall;                                       // Sep-Nov
}; 